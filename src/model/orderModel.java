package model;

/**
 *
 * @author Heshan
 */
public class orderModel {
    
    private int qty;
    private double amount;
    //private 

    public orderModel(int qty, double amount) {
        this.qty = qty;
        this.amount = amount;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }
    
    
    
}
